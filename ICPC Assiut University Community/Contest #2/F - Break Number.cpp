int countDivisions(long long x) {
    int count = 0;
    while (x % 2 == 0) {
        x /= 2;
        count++;
    }
    return count;
}
int main() {
    int N;
    cin >> N;

    vector<long long> numbers(N);
    for (int i = 0; i < N; i++) {
        cin >> numbers[i];
    }

    int maxDivisions = 0;
    for (int i = 0; i < N; i++) {
        int divisions = countDivisions(numbers[i]);
        maxDivisions = max(maxDivisions, divisions);
    }

    cout << maxDivisions << endl;

    return 0;
}








